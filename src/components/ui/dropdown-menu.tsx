// Define the type for each language item
interface Language {
  name: string;
  flag: string;
}

// Define the props for the LanguageDropdown component
interface LanguageDropdownProps {
  languages: Language[]; // Accepts an array of languages
}

const LanguageDropDownMenu: React.FC<LanguageDropdownProps> = ({
  languages,
}) => {
  const [selectedLanguage, setSelectedLanguage] = React.useState<Language>(
    languages[0]
  );

  return (
    <DropdownMenuPrimitive.Root>
      <DropdownMenuPrimitive.Trigger className="box-border h-[2.5rem] px-4 py-2 bg-white text-[#667185] rounded flex items-center cursor-pointer font-semibold text-[1rem] group">
        <Image src={selectedLanguage.flag} width={16} height={16} alt="flag" />
        <span className="ml-2">{selectedLanguage.name}</span>
        <ChevronDown
          className={`ml-2 transition duration-200 group-data-[state=open]:rotate-180`}
        />
      </DropdownMenuPrimitive.Trigger>

      {/* Dropdown Menu */}
      <DropdownMenuPrimitive.Content
        align="start"
        sideOffset={5}
        className="w-[7.5rem] bg-white shadow-md text-[#667185] text-[1rem] font-semibold rounded-md"
      >
        {languages.map((lang, index) => (
          <DropdownMenuPrimitive.Item
            key={index}
            onSelect={() => setSelectedLanguage(lang)}
            className="flex items-center px-4 py-2 hover:bg-gray-200"
          >
            <Image
              src={selectedLanguage.flag}
              width={16}
              height={16}
              alt="flag"
            />
            <span className="ml-2">{lang.name}</span>
          </DropdownMenuPrimitive.Item>
        ))}
      </DropdownMenuPrimitive.Content>
    </DropdownMenuPrimitive.Root>
  );
};

